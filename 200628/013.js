for (var i = 0; i < 10; i++){
    switch (i)
    {
        case 1 :
            console.log(i + 'st');
            break;
        case 2 :
            console.log(i + 'nd');
            break;
        case 3 :
            console.log(i + 'rd');
            break;
        default :
           console.log(i + 'th');
            break;
    }
}